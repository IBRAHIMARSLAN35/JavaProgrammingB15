package day05;

public class variablePractices {
    public static void main(String[] args) {
        int birthYear=1985;
        int currentYear=2019;
        int age=currentYear-birthYear;
        System.out.println("I was born in "+birthYear+" and I am "+age+" years old.");

        int speed=75;
        int limit=65;
        int over=speed-limit;
        System.out.println("Your speed is "+speed+" and you are more than "+over+" mph over the speed limit.");
    }

}
