package day07;

public class Casting {
    public static void main(String[] args) {

        long a=10L;
        int b= (int) a;
        System.out.println(a);

        System.out.println(b > 5);
        System.out.println(b >= 5);
        System.out.println(b <= 5);
        System.out.println(b < 5);




    }

}
