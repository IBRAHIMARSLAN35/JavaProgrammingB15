package day09;
import java.util.Scanner;

public class play1 {
        public static void main(String[] args) {
            Scanner scan=new Scanner(System.in);
            int option=scan.nextInt();
            switch (option){
                case 1 :
                    System.out.println("HELLO SDET");
                    break;
                case 2 :
                    System.out.println("HOLA SDET");
                    break;
                case 3:
                    System.out.println("BONJOUR SDET");
                    break;
                case 4:
                    System.out.println("MERHABA SDET");
                    break;
                case 5:
                    System.out.println("SZAI SDET");
                    break;
                case 6:
                    System.out.println("HI SDET");
                    break;
                case 7:
                    System.out.println("HELLOOOO SDET");
                    break;
                default:
                    System.out.println("Unknown language.");



        }
    }

}
