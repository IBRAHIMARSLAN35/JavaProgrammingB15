package day09;
import java.util.Scanner;
public class LanguagePicker {
    public static void main(String[] args) {
        Scanner scan=new Scanner(System.in);
        int option=scan.nextInt();
        String Language="";
        if(option==1){Language="Hello";}
        else if(option==2){Language="Hello";}
        else if (option==3){Language="Salam";}
        else if (option==4){Language="Hola";}
        else if (option==5){Language="Privet";}
        else if (option==6){Language="Merhaba";}
        else if (option==7){Language="Szia";}
        System.out.println(Language+ " SDET");




            }
}
